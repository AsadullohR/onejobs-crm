const LANG_KEY = "crm_lang";
const DEFAULT_LANG = "uz";

const LangCtx = createContext({ lang: DEFAULT_LANG, t: k => k, setLang: () => {} });
const useLang = () => useContext(LangCtx);

function LangProvider({ children }) {
  const [lang, setLangState] = useState(() => {
    try { return localStorage.getItem(LANG_KEY) || DEFAULT_LANG; } catch { return DEFAULT_LANG; }
  });

  const setLang = useCallback(l => {
    setLangState(l);
    try { localStorage.setItem(LANG_KEY, l); } catch {}
  }, []);

  const t = useCallback((key, fallback) => {
    return LANGS[lang]?.[key] ?? LANGS[DEFAULT_LANG]?.[key] ?? fallback ?? key;
  }, [lang]);

  return <LangCtx.Provider value={{ lang, t, setLang }}>{children}</LangCtx.Provider>;
}

export { LangProvider, useLang, LANGS };
